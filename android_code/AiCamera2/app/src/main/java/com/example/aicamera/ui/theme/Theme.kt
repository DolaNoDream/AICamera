/**
 * 核心主题函数，整个应用都要用它包裹。
 *
 * 说明：
 * - 默认使用浅色主题（更贴近系统相册风格），避免某些机型/ROM 在深色模式下出现异常。
 * - 用 Surface 承接 background，保证窗口背景与 Material3 配色一致。
 */
@Composable
fun AICameraTheme(
    darkTheme: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        shapes = AppShapes
    ) {
        // 不再覆盖 LocalIndication（避免依赖不存在的 ripple 包）。
        // clickables 的兼容性由各组件内部使用 overload(clickable(indication = ...)) 来保证。
        Surface(color = MaterialTheme.colorScheme.background) {
            content()
        }
    }
}
